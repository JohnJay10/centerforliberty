                                <p class="bold-text">
                                    Today, Monday, the 22nd of June, 2020, CFL is pleased to announce the commencement of a Digital Freedom Advocacy which is being sponsored by <a href="https://voice.global/grantees/digital-freedom-advocacy/" style="color:green; font-size:large; font-weight:bold;"> Voice Nigeria</a> and aimed at facilitating citizens’ engagement on digital freedom, with the specific goal of ensuring the withdrawal of legislative bills threatening citizens’ rights to digital freedom and freedom of expression, as guaranteed by Nigeria’s constitutional democracy.
                                </p>
                                        
                                <p class="bold-text">
                                    The Digital Freedom Advocacy is a citizens-led intervention that will be implemented using the hashtag #InternetNoBeEnemy, to mobilize Nigerians to ensure the withdrawal of the “Protection from Internet Falsehood and Manipulation Bill 2019” which was overwhelmingly rejected at the public hearing on March 9, 2020; as well as the withdrawal of the “National Commission for the Prohibition of Hate Speeches Bill, 2019” – which is still at its introductory stage on the floor of the Nigerian Senate.
                                </p>
                                
                                <p class="bold-text">
                                    The Nigerian Senate is invited to understudy how internet censorship, intimidation of medical practitioners, the clampdown on whistle-blowers and forceful suppression of information on COVID-19 outbreak in Wuhan district in China, had prevented the world from learning and preparing early for the deadly virus, which has now infected over eight million people worldwide. It is now being said that China’s failure to alert the world to the outbreak of the novel virus, had impeded a rapid global action and response to the COVID-19 pandemic, which has led to the deaths of nearly five hundred thousand people across the world, including over five hundred Nigerians. We believe that the inestimable role of digital platforms, including social media, in Nigeria, has been well-accentuated throughout Nigeria’s response and fight against COVID-19 so far. Since Nigeria’s first confirmed case of coronavirus in late February, the social media and other digital platforms have been extremely invaluable in the areas of awareness, information dissemination and widespread reporting of human rights violations, security challenges and economic crisis in the country. These platforms have also been instrumental in mainstreaming the harsh economic conditions under which the majority of Nigerians have had to endure the lockdown in specific states, thereby prodding the Federal Government to commence a palliative distribution program.
                                </p>
                                
                                <p class="bold-text">
                                    We have also seen how digital platforms have been at the forefront of demanding transparency and accountability during these trying times, specifically concerning palliative distribution in specific states across the country. Also, the Social Media was very instrumental in ensuring the dismissal of two police officers, who had physically assaulted and brutalized Ms Tola Azeez, whilst on her way to acquiring emergency drugs for her relatives in Iwo, Osun State. In the same vein, the social media has played a vital role in reporting several acts of disregard for COVID-19 directives and executive overreach by some state governors, just as it has been at the vanguard of demanding the release of a Sports Journalist known as Kufre Carter, who was arrested by the Department of State Services (DSS) in Akwa Ibom, for “castigating” the state’s commissioner for health for his approach in fighting COVID19 in the state, and has been in detention without access to his family and lawyer since the 27th of April, 2020. And now, social media is leading the global fight against Sexual and Gender-Based Violence and the rape pandemic in Nigeria.
                                </p>
                                
                                <p class="bold-text">
                                    Despite the COVID-19 pandemic and the physical and mental isolation which Nigerians and indeed global citizens have had to put up with in recent times, the social media and other digital platforms have not only been the fountain of inspiration to Nigerians, but they have also played a conspicuous role in accentuating the essence of our democracy – freedom; thereby allowing citizens to hold their elected representatives accountable and at the same time demand better representation. It will, therefore, be a grave mistake for the Nigerian Senate to make Nigeria vulnerable to a potential global health, economic or political crisis as a result of the passage of the social media bill and the hate speech bill. It is, perhaps, also important to note that while CFL admits that occasionally, there are instances of false information on social media platforms, we have also observed that such information is often quickly debunked by either the concerned media platform’s fact-checking mechanisms or by other internet users who have personal knowledge of a more accurate information. The consistency of this act of selfregulation on digital platforms accentuates the essence of digital freedoms in any democratic society.
                                </p>
                                
                                <p class="bold-text">
                                    Despite the COVID-19 pandemic and the physical and mental isolation which Nigerians and indeed global citizens have had to put up with in recent times, the social media and other digital platforms have not only been the fountain of inspiration to Nigerians, but they have also played a conspicuous role in accentuating the essence of our democracy – freedom; thereby allowing citizens to hold their elected representatives accountable and at the same time demand better representation. It will, therefore, be a grave mistake for the Nigerian Senate to make Nigeria vulnerable to a potential global health, economic or political crisis as a result of the passage of the social media bill and the hate speech bill.
                                </p>
                                
                                <p class="bold-text">
                                    It is for these reasons that we wish to urge the Nigerian Senate, to immediately kill the social media bill and the hate speech bill, as their lingering existence in the legislative chamber would continue to pose an enormous danger to democratic security and the constitutionally guaranteed rights of all Nigerians, including the lawmakers, too.
                                </p>
                                
                                
                                <p class="bold-text">
                                    The Internet is not the enemy <a href="https://twitter.com/search?q=%23InternetNoBeEnemy&src=typed_query" style="color:green; font-size:large; font-weight:bold;">#InternetNoBeEnemy</a>.
                                </p>
                                
